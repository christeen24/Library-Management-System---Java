    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String sql="select * from Login where UserName=? and Password=?";
        try{
            pst=conn.prepareStatement(sql);
            pst.setString(1,jTextField1.getText());
            pst.setString(2,jPasswordField1.getText().toString());
            rs=pst.executeQuery();
            if(rs.next()){
                rs.close();
                pst.close();
                Homeclass hc = new Homeclass();
                hc.setuname(String.valueOf(jTextField1.getText()));
                JOptionPane.showMessageDialog(null, "Welcome to the LMS!");
                
                setVisible(false);
                Home ob=new Home(hc);
                ob.setVisible(true);
                
            }else{
                JOptionPane.showMessageDialog(null, "Incorrect User Name and Password");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);  
        }finally{
                    try{
                    rs.close();
                    pst.close();
                    }catch(Exception e){
                        JOptionPane.showMessageDialog(null, e);
                    }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

