    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
            
            try{
            String sql="Insert into Book(BookID,Name,Edition,Author,Price,Pages) values(?,?,?,?,?,?)";
            
                pst=conn.prepareStatement(sql);
                
                pst.setString(1, t1.getText());
                pst.setString(2, t2.getText());
                pst.setString(3, (String) jComboBox1.getSelectedItem());
                pst.setString(4, t4.getText());
                pst.setString(5, t5.getText());
                pst.setString(6, t6.getText());
            
                pst.execute();
                pst.close();
                
                JOptionPane.showMessageDialog(null, "New Book is Added.");
                
                setVisible(false);
                Home ob=new Home();
                ob.setVisible(true);
            
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
            setVisible(false);
            Home ob=new Home();
            ob.setVisible(true);
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

